package main

import "fmt"

/*
Operadores:
-> + : adição
-> -: substração,
-> *: multiplicação,
-> /: divisão,
-> %: resto de divisão
-> ==: igual
-> !=: diferente
-> <: menor que
-> >: maior que
-> <= menor ou igual
-> >=: maior ou egual
*/

func main() {
	fmt.Println(2 + 2)
	fmt.Println(2 - 2)
	fmt.Println(2 * 6)
	fmt.Println(7 % 5)
	fmt.Println(2 == 2)
}
